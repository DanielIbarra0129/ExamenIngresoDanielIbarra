package co.com.ceiba.mobile.pruebadeingreso.MVP.modelo.entitis;

import android.arch.persistence.room.Entity;
import android.arch.persistence.room.PrimaryKey;
import android.support.annotation.NonNull;

import com.google.gson.annotations.SerializedName;

import java.io.Serializable;

import co.com.ceiba.mobile.pruebadeingreso.MVP.modelo.entitis.Address;
import co.com.ceiba.mobile.pruebadeingreso.MVP.modelo.entitis.Company;

@Entity(tableName = "user_table")
public class User implements Serializable {
    @PrimaryKey
    @NonNull
    private String id;
    private String name;
    @SerializedName(value = "username")
    private String userName;
    private String email;
    private Address address;
    private String phone;
    private String website;
    private Company company;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public Company getCompany() {
        return company;
    }

    public void setCompany(Company company) {
        this.company = company;
    }

}

